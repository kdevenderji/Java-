package com.company;

public class RelationalOperators {
    public static void main(String []args){
        int a= 10;
        int b=5;
//        == operators
        System.out.println(a==b);

//        != operators
        System.out.println(a!=b);
//         > operators
        System.out.println(a>b);

//        < operators
        System.out.println(a<b);

//        >= operators
        System.out.println(a>=b);

//        <= operators
        System.out.println(a<=b);


    }
}
