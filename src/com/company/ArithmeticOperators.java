package com.company;

public class ArithmeticOperators {
    public static void main(String[] args){
        int a= 10;
        int b= 3;
//    Addition
        System.out.println(a+b);

//        Subtraction
        System.out.println(a-b);

//      Multiplication
        System.out.println(a*b);

//        Division
        System.out.println(a/(float)b);

//        Modulo Operator
        System.out.println(a%b);
    }
}
